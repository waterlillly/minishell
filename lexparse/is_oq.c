#include "../minishell.h"

int	is_oq(char	c, int *dq, int *sq)
{
	if (c == '\"' && *sq % 2 == 0)
		*dq = *dq + 1;
	if (c == '\'' && *dq % 2 == 0)
		*sq = *sq + 1;
	if (*sq % 2 == 0 && *dq % 2 == 0)
		return (0);
	return (1);
}