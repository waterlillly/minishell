#include "../minishell.h"

char	*skip_q(char *str)
{
	if (*str == '\"')
	{
		str++;
		while (*str != '\"')
			str++;
	}
	else if (*str == '\'')
	{
		str++;
		while (*str != '\'')
			str++;
	}
	return (str);
}